use std::{
    fs,
    path::{Path, PathBuf},
    time::Instant,
};

use dust_cache::WorkspaceCache;
use dust_diagnostics::Diagnostic;
use dust_workspace::detect_workspace_root;

use crate::{CleanReport, CleanRequest, CommandResult};

const DUST_HEADER_LINE_1: &str = "// GENERATED CODE - DO NOT MODIFY BY HAND";
const DUST_HEADER_LINE_2: &str = "// Generated by dust.";

/// Removes Dust-generated `.g.dart` files plus `.dart_tool/dust` cache state.
pub fn run_clean(request: CleanRequest) -> CommandResult {
    let started = Instant::now();
    let mut result = CommandResult::default();

    let root = match detect_workspace_root(&request.cwd) {
        Ok(root) => root,
        Err(diagnostic) => {
            result.diagnostics.push(diagnostic);
            result.elapsed_ms = started.elapsed().as_millis();
            return result;
        }
    };

    let mut generated_files = Vec::new();
    if let Err(diagnostic) = collect_generated_files(&root.join("lib"), &mut generated_files) {
        result.diagnostics.push(diagnostic);
        result.elapsed_ms = started.elapsed().as_millis();
        return result;
    }

    let scanned_files = generated_files.len();
    let mut removed_files = 0usize;

    for output_path in generated_files {
        match is_dust_generated_file(&output_path) {
            Ok(true) => match fs::remove_file(&output_path) {
                Ok(()) => removed_files += 1,
                Err(error) => result.diagnostics.push(Diagnostic::error(format!(
                    "failed to remove Dust output `{}`: {error}",
                    output_path.display()
                ))),
            },
            Ok(false) => {}
            Err(error) => result.diagnostics.push(Diagnostic::error(format!(
                "failed to inspect generated file `{}`: {error}",
                output_path.display()
            ))),
        }
    }

    let cache_cleared = if result.has_errors() {
        false
    } else {
        match WorkspaceCache::delete_storage(&root) {
            Ok(cache_cleared) => cache_cleared,
            Err(error) => {
                result.diagnostics.push(Diagnostic::error(format!(
                    "failed to remove Dust cache storage under `{}`: {error}",
                    root.join(".dart_tool/dust").display()
                )));
                false
            }
        }
    };

    result.clean = Some(CleanReport {
        root,
        scanned_files,
        removed_files,
        cache_cleared,
    });
    result.elapsed_ms = started.elapsed().as_millis();
    result
}

fn collect_generated_files(dir: &Path, out: &mut Vec<PathBuf>) -> Result<(), Diagnostic> {
    if !dir.is_dir() {
        return Ok(());
    }

    let mut entries = fs::read_dir(dir)
        .map_err(|error| {
            Diagnostic::error(format!(
                "failed to read directory `{}`: {error}",
                dir.display()
            ))
        })?
        .collect::<Result<Vec<_>, _>>()
        .map_err(|error| {
            Diagnostic::error(format!(
                "failed to enumerate directory `{}`: {error}",
                dir.display()
            ))
        })?;
    entries.sort_by_key(|entry| entry.path());

    for entry in entries {
        let path = entry.path();
        if path.is_dir() {
            collect_generated_files(&path, out)?;
        } else if is_generated_dart_file(&path) {
            out.push(path);
        }
    }

    Ok(())
}

fn is_generated_dart_file(path: &Path) -> bool {
    path.file_name()
        .and_then(|name| name.to_str())
        .is_some_and(|name| name.ends_with(".g.dart"))
}

fn is_dust_generated_file(path: &Path) -> Result<bool, std::io::Error> {
    let contents = fs::read_to_string(path)?;
    let mut lines = contents.lines();
    Ok(matches!(
        (lines.next(), lines.next()),
        (Some(DUST_HEADER_LINE_1), Some(DUST_HEADER_LINE_2))
    ))
}

#[cfg(test)]
mod tests {
    use tempfile::tempdir;

    use super::*;

    #[test]
    fn dust_generated_file_requires_exact_header_prefix() {
        let temp = tempdir().unwrap();
        let owned = temp.path().join("owned.g.dart");
        let foreign = temp.path().join("foreign.g.dart");

        fs::write(
            &owned,
            "// GENERATED CODE - DO NOT MODIFY BY HAND\n// Generated by dust.\npart of 'x.dart';\n",
        )
        .unwrap();
        fs::write(&foreign, "part of 'x.dart';\n// Generated by dust.\n").unwrap();

        assert!(is_dust_generated_file(&owned).unwrap());
        assert!(!is_dust_generated_file(&foreign).unwrap());
    }

    #[test]
    fn collect_generated_files_only_returns_g_dart_files_in_sorted_order() {
        let temp = tempdir().unwrap();
        let lib_dir = temp.path().join("lib");
        let nested = lib_dir.join("nested");
        fs::create_dir_all(&nested).unwrap();
        fs::write(lib_dir.join("a.g.dart"), "").unwrap();
        fs::write(lib_dir.join("b.dart"), "").unwrap();
        fs::write(nested.join("c.g.dart"), "").unwrap();

        let mut files = Vec::new();
        collect_generated_files(&lib_dir, &mut files).unwrap();

        assert_eq!(files.len(), 2);
        assert!(files[0].ends_with("a.g.dart"));
        assert!(files[1].ends_with("c.g.dart"));
    }

    #[test]
    fn clean_skips_cache_deletion_when_output_removal_fails() {
        let temp = tempdir().unwrap();
        let root = temp.path();
        let lib_dir = root.join("lib");
        let dart_tool_dir = root.join(".dart_tool/dust");
        fs::create_dir_all(&lib_dir).unwrap();
        fs::create_dir_all(&dart_tool_dir).unwrap();
        fs::write(root.join("pubspec.yaml"), "name: sample\n").unwrap();
        fs::write(
            root.join(".dart_tool/package_config.json"),
            r#"{"configVersion":2,"packages":[]}"#,
        )
        .unwrap();
        let owned = lib_dir.join("user.g.dart");
        fs::write(
            &owned,
            "// GENERATED CODE - DO NOT MODIFY BY HAND\n// Generated by dust.\n",
        )
        .unwrap();

        let mut perms = fs::metadata(&lib_dir).unwrap().permissions();
        perms.set_readonly(true);
        fs::set_permissions(&lib_dir, perms).unwrap();

        let result = run_clean(CleanRequest {
            cwd: root.to_path_buf(),
        });

        let mut perms = fs::metadata(&lib_dir).unwrap().permissions();
        #[allow(clippy::permissions_set_readonly_false)]
        {
            perms.set_readonly(false);
        }
        fs::set_permissions(&lib_dir, perms).unwrap();

        assert!(result.has_errors());
        assert!(owned.exists());
        assert!(dart_tool_dir.exists());
        assert!(!result.clean.unwrap().cache_cleared);
    }
}
