use std::{thread, time::Duration};

use dust_cli::run_cli;

use super::helpers::{make_workspace, write_file};

#[test]
fn cli_clean_removes_dust_outputs_and_cache() {
    let workspace = make_workspace();
    write_file(
        &workspace.path().join("lib/user.dart"),
        "part 'user.g.dart';\n\
         @ToString()\n\
         class User {\n\
           final String id;\n\
           const User(this.id);\n\
         }\n",
    );

    let built = run_cli(["build", "--root", workspace.path().to_str().unwrap()]);
    assert_eq!(built.exit_code, 0);
    assert!(workspace.path().join("lib/user.g.dart").exists());
    assert!(
        workspace
            .path()
            .join(".dart_tool/dust/build_cache_v1.json")
            .exists()
    );

    write_file(
        &workspace.path().join("lib/foreign.g.dart"),
        "// generated by someone else\n",
    );

    let run = run_cli(["clean", "--root", workspace.path().to_str().unwrap()]);

    assert_eq!(run.exit_code, 0);
    assert!(run.stderr.is_empty());
    assert!(run.stdout.contains("clean  scanned: 2"));
    assert!(run.stdout.contains("removed: 1"));
    assert!(run.stdout.contains("cache: cleared"));
    assert!(!workspace.path().join("lib/user.g.dart").exists());
    assert!(workspace.path().join("lib/foreign.g.dart").exists());
    assert!(!workspace.path().join(".dart_tool/dust").exists());
}

#[test]
fn cli_check_returns_stale_exit_code_before_build() {
    let workspace = make_workspace();
    write_file(
        &workspace.path().join("lib/user.dart"),
        "part 'user.g.dart';\n\
         @ToString()\n\
         class User {\n\
           final String id;\n\
           const User(this.id);\n\
         }\n",
    );

    let run = run_cli(["check", "--root", workspace.path().to_str().unwrap()]);

    assert_eq!(run.exit_code, 2);
    assert!(run.stdout.is_empty());
    assert!(run.stderr.contains("stale"));
}

#[test]
fn cli_watch_reports_rebuilt_library() {
    let workspace = make_workspace();
    write_file(
        &workspace.path().join("lib/user.dart"),
        "part 'user.g.dart';\n\
         @ToString()\n\
         class User {\n\
           final String id;\n\
           const User(this.id);\n\
         }\n",
    );

    let root = workspace.path().to_path_buf();
    let user_path = root.join("lib/user.dart");
    let modifier = thread::spawn(move || {
        thread::sleep(Duration::from_millis(80));
        write_file(
            &user_path,
            "part 'user.g.dart';\n\
             @ToString()\n\
             class User {\n\
               final String id;\n\
               final int age;\n\
               const User(this.id, this.age);\n\
             }\n",
        );
    });

    let run = run_cli([
        "watch",
        "--root",
        workspace.path().to_str().unwrap(),
        "--poll-ms",
        "20",
        "--max-cycles",
        "8",
    ]);
    modifier.join().unwrap();

    assert_eq!(run.exit_code, 0);
    assert!(run.stdout.contains("watch  scanned:"));
    assert!(run.stdout.contains("rebuilds: 1"));
}
