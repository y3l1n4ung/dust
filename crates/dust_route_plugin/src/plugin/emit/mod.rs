pub(crate) fn render_route_generated(library: &LibraryIr, spec: &RouterSpec) -> String {
    let mut out = String::new();
    out.push_str("// GENERATED CODE - DO NOT MODIFY BY HAND.\n");
    out.push_str("// Generated by dust.\n\n");
    out.push_str("// ignore_for_file: unused_import, unnecessary_import\n\n");
    out.push_str("import 'package:flutter/material.dart' hide Route;\n\n");
    out.push_str("import 'route.dart';\n");
    out.push_str("import 'package:dust_router/dust_router.dart';\n");
    let current_import = package_import_uri(library);
    let imports = spec
        .routes
        .iter()
        .flat_map(|route| {
            route
                .import_uri
                .iter()
                .map(String::as_str)
                .chain(route.imports.iter().map(String::as_str))
        })
        .filter(|import| Some(*import) != current_import.as_deref())
        .filter(|import| !matches!(*import, "route.g.dart" | "routing_core.dart"))
        .collect::<BTreeSet<_>>();
    for import in imports {
        out.push_str(&format!("import '{import}';\n"));
    }
    out.push('\n');
    if uses_no_transition_builder(spec) {
        render_no_transition_builder(&mut out);
    }

    out.push_str(&format!(
        "abstract class {} extends DustRouterBase<AppRoutePath> {{\n",
        spec.generated_base_class
    ));
    out.push_str("  RouterConfig<AppRoutePath> get config {\n");
    out.push_str("    final runtimeConfig = DustRouterConfig<AppRoutePath>(\n");
    out.push_str("      router: this,\n");
    out.push_str(&format!(
        "      initialRoute: const {}(),\n",
        spec.initial_route_class
    ));
    out.push_str("      parseRoute: parseAppRoute,\n");
    out.push_str("      routeLocation: routeLocation,\n");
    out.push_str("      requiresAuth: routeRequiresAuth,\n");
    out.push_str("      resolveGuards: (route) => routeGuards(route, this),\n");
    out.push_str("      buildPage: buildAppRoutePage,\n");
    out.push_str("    );\n");
    out.push_str("    final delegate = DustRouterDelegate<AppRoutePath>(runtimeConfig);\n");
    out.push_str("    return RouterConfig<AppRoutePath>(\n");
    out.push_str("      routeInformationProvider: PlatformRouteInformationProvider(\n");
    out.push_str("        initialRouteInformation: RouteInformation(\n");
    out.push_str(
        "          uri: Uri.parse(WidgetsBinding.instance.platformDispatcher.defaultRouteName),\n",
    );
    out.push_str("        ),\n");
    out.push_str("      ),\n");
    out.push_str("      routeInformationParser: DustRouteInformationParser<AppRoutePath>(\n");
    out.push_str("        parseRoute: parseAppRoute,\n");
    out.push_str("        routeLocation: routeLocation,\n");
    out.push_str("      ),\n");
    out.push_str("      routerDelegate: delegate,\n");
    out.push_str("      backButtonDispatcher: RootBackButtonDispatcher(),\n");
    out.push_str("    );\n");
    out.push_str("  }\n");
    render_guard_factories(&mut out, spec);
    out.push_str("}\n\n");

    metadata::render_route_metadata(&mut out, &spec.routes);
    render_route_classes(&mut out, spec);
    render_helpers(&mut out, spec);
    render_parser(&mut out, spec);
    render_shell_consistency_helpers(&mut out, spec);
    render_page_builder(&mut out, spec);
    format_dart_source(&out).unwrap_or(out)
}

fn render_no_transition_builder(out: &mut String) {
    out.push_str(
        "class _NoTransitionBuilder extends PageTransitionsBuilder {\n\
  const _NoTransitionBuilder();\n\n\
  @override\n\
  Widget buildTransitions<T>(\n\
    PageRoute<T> route,\n\
    BuildContext context,\n\
    Animation<double> animation,\n\
    Animation<double> secondaryAnimation,\n\
    Widget child,\n\
  ) {\n\
    return child;\n\
  }\n\
}\n\n",
    );
}

fn uses_no_transition_builder(spec: &RouterSpec) -> bool {
    spec.routes.iter().any(|route| {
        route
            .annotation
            .transition
            .as_deref()
            .is_some_and(|transition| transition.contains("_NoTransitionBuilder"))
    })
}

fn format_dart_source(source: &str) -> Option<String> {
    let mut child = Command::new("dart")
        .args(["format", "--output=show", "--stdin-name", "route.g.dart"])
        .stdin(Stdio::piped())
        .stdout(Stdio::piped())
        .stderr(Stdio::null())
        .spawn()
        .ok()?;
    child.stdin.as_mut()?.write_all(source.as_bytes()).ok()?;
    let output = child.wait_with_output().ok()?;
    if !output.status.success() {
        return None;
    }
    String::from_utf8(output.stdout).ok()
}

fn render_guard_factories(out: &mut String, spec: &RouterSpec) {
    for guard in &spec.guard_classes {
        out.push_str(&format!(
            "\n  {guard} create{}();\n",
            upper_camel_identifier(guard)
        ));
    }
}

fn write_indent(out: &mut String, indent: usize) {
    out.push_str(&indent_str(indent));
}

fn indent_str(indent: usize) -> String {
    "  ".repeat(indent)
}

pub(super) enum RenderedField {
    Line(String),
    Inline(String),
}

impl RenderedField {
    pub(super) fn line(value: impl Into<String>) -> Self {
        Self::Line(value.into())
    }

    pub(super) fn inline(value: impl Into<String>) -> Self {
        Self::Inline(value.into())
    }

    pub(super) fn render(self, out: &mut String, indent: usize) {
        match self {
            Self::Line(value) => {
                write_indent(out, indent);
                out.push_str(&value);
                out.push('\n');
            }
            Self::Inline(value) => {
                if !out.ends_with('\n') {
                    out.push('\n');
                }
                write_indent(out, indent);
                out.push_str(&value);
            }
        }
    }
}

fn render_route_classes(out: &mut String, spec: &RouterSpec) {
    out.push_str("typedef RouteState = DustRouteState<AppRoutePath>;\n\n");
    out.push_str("sealed class AppRoutePath {\n  const AppRoutePath();\n\n  String get location;\n\n  /// Defaults to true. Override and return false for public routes\n  /// (login, invite, forbidden, checkout, notFound).\n  bool get requiresAuth => true;\n}\n\n");

    for route in &spec.routes {
        out.push_str(&format!(
            "final class {} extends AppRoutePath {{\n",
            route.route_class
        ));
        let constructor_params = route
            .params
            .iter()
            .map(render_constructor_param)
            .collect::<Vec<_>>()
            .join(", ");
        if constructor_params.is_empty() {
            out.push_str(&format!("  const {}();\n\n", route.route_class));
        } else {
            out.push_str(&format!(
                "  const {}({{{constructor_params}}});\n\n",
                route.route_class
            ));
        }
        for param in &route.params {
            out.push_str(&format!(
                "  final {} {};\n",
                dart_type(&param.ty),
                param.name
            ));
        }
        if !route.params.is_empty() {
            out.push('\n');
        }
        out.push_str("  @override\n  String get location {\n");
        render_location_body(out, route);
        out.push_str("  }\n");
        if route.annotation.guards_configured && route.annotation.guards.is_empty() {
            out.push_str("\n  @override\n  bool get requiresAuth => false;\n");
        }
        out.push_str("}\n\n");
    }
}

fn render_helpers(out: &mut String, spec: &RouterSpec) {
    out.push_str("String routeLocation(AppRoutePath route) => route.location;\n\n");
    out.push_str(
        "String _routePath(List<String> segments, {Map<String, String>? queryParameters}) {\n",
    );
    out.push_str("  final query = queryParameters?.isEmpty ?? true ? null : queryParameters;\n");
    out.push_str(
        "  final text = Uri(pathSegments: segments, queryParameters: query).toString();\n",
    );
    out.push_str("  if (text.isEmpty) return '/';\n");
    out.push_str("  return text.startsWith('/') ? text : '/$text';\n");
    out.push_str("}\n\n");
    out.push_str("bool routeRequiresAuth(AppRoutePath route) => route.requiresAuth;\n\n");
    out.push_str(
        "List<RouteGuard<AppRoutePath>> routeGuards(AppRoutePath route, $AppRouter router) {\n",
    );
    out.push_str("  return switch (route) {\n");
    for route in &spec.routes {
        if route.annotation.guards.is_empty() {
            continue;
        }
        let pattern = format!("{}()", route.route_class);
        let guards = route
            .annotation
            .guards
            .iter()
            .map(|guard| format!("router.create{}()", upper_camel_identifier(guard)))
            .collect::<Vec<_>>()
            .join(", ");
        out.push_str(&format!("    {pattern} => [{guards}],\n"));
    }
    out.push_str("    _ => const [],\n");
    out.push_str("  };\n");
    out.push_str("}\n\n");
    out.push_str("extension DustRouterContext on BuildContext {\n");
    out.push_str(
        "  DustRouterController<AppRoutePath> get router => DustRouter.of<AppRoutePath>(this);\n",
    );
    out.push_str("  AppRoutesNavigation get routes => AppRoutesNavigation(router);\n");
    out.push_str("}\n\n");
    out.push_str("final class AppRoutesNavigation {\n");
    out.push_str("  const AppRoutesNavigation(this._router);\n\n");
    out.push_str("  final DustRouterController<AppRoutePath> _router;\n\n");
    for route in &spec.routes {
        out.push_str(&format!(
            "  RouteNavigation<AppRoutePath> {}({}) => RouteNavigation(\n    _router,\n    {}({}),\n  );\n\n",
            route.name,
            render_factory_params(route),
            route.route_class,
            render_route_args(route)
        ));
    }
    out.push_str("  void pop() => _router.pop();\n");
    out.push_str("}\n\n");
    out.push_str("final class RouteNavigation<T extends Object> {\n");
    out.push_str("  const RouteNavigation(this._router, this.route);\n\n");
    out.push_str("  final DustRouterController<T> _router;\n  final T route;\n\n");
    out.push_str("  void go() => _router.go(route);\n  void push() => _router.push(route);\n  void replace() => _router.replace(route);\n");
    out.push_str("}\n\n");
}

fn render_parser(out: &mut String, spec: &RouterSpec) {
    out.push_str("AppRoutePath parseAppRoute(Uri uri) {\n");
    out.push_str("  final segments = uri.pathSegments;\n\n");
    for route in &spec.routes {
        render_parse_case(out, route);
    }
    out.push_str("  return _notFoundRoute(uri);\n");
    out.push_str("}\n\n");
    let fallback = spec
        .not_found_route_class
        .as_deref()
        .and_then(|class| {
            spec.routes
                .iter()
                .find(|route| route.route_class == class)
                .map(|route| {
                    if is_not_found_route(route) {
                        "NotFoundRoute(path: uri.toString())".to_owned()
                    } else {
                        route_constructor_with_fallback(route, "uri.toString()")
                    }
                })
        })
        .unwrap_or_else(|| format!("const {}()", spec.initial_route_class));
    out.push_str(&format!(
        "AppRoutePath _notFoundRoute(Uri uri) => {fallback};\n\n"
    ));
    out.push_str("bool? _parseBool(String? value) {\n");
    out.push_str("  return switch (value) {\n    'true' || '1' => true,\n    'false' || '0' => false,\n    null || '' => null,\n    _ => () {\n      assert(false, '_parseBool: unrecognised value \"$value\", treating as null');\n      return null;\n    }(),\n  };\n");
    out.push_str("}\n\n");
}

fn render_shell_consistency_helpers(out: &mut String, spec: &RouterSpec) {
    out.push_str("const Map<Type, Type?> _kAppliedShellsByPage = {\n");
    for route in &spec.routes {
        out.push_str(&format!(
            "  {}: {},\n",
            route.page_class,
            effective_shell(route, &spec.routes).unwrap_or("null")
        ));
    }
    out.push_str("};\n\n");
    out.push_str("bool _shellConsistencyCheck() {\n");
    out.push_str("  bool visit(GeneratedRoute route) {\n");
    out.push_str("    final page = route.page;\n");
    out.push_str("    if (page != null && _kAppliedShellsByPage[page] != route.shell) {\n");
    out.push_str("      return false;\n");
    out.push_str("    }\n");
    out.push_str("    return route.routes.every(visit);\n");
    out.push_str("  }\n");
    out.push_str("  return $appRoutes.every(visit);\n");
    out.push_str("}\n\n");
}

fn render_page_builder(out: &mut String, spec: &RouterSpec) {
    out.push_str("Page<void> buildAppRoutePage(AppRoutePath route) {\n");
    out.push_str("  assert(\n");
    out.push_str("    _shellConsistencyCheck(),\n");
    out.push_str("    'Shell mismatch between \\$appRoutes and buildAppRoutePage',\n");
    out.push_str("  );\n");
    out.push_str("  return switch (route) {\n");
    for route in &spec.routes {
        let pattern = if route.params.is_empty() {
            format!("{}()", route.route_class)
        } else {
            format!(
                "{}({})",
                route.route_class,
                route
                    .params
                    .iter()
                    .map(|param| format!("{}: final {}", param.name, param.name))
                    .collect::<Vec<_>>()
                    .join(", ")
            )
        };
        let page_args = route
            .params
            .iter()
            .map(|param| format!("{}: {}", param.name, param.name))
            .collect::<Vec<_>>()
            .join(", ");
        let child = if page_args.is_empty() {
            format!("const {}()", route.page_class)
        } else {
            format!("{}({page_args})", route.page_class)
        };
        let child = if let Some(shell) = effective_shell(route, &spec.routes) {
            format!("{shell}(child: {child})")
        } else {
            child
        };
        let transition_arg = route
            .annotation
            .transition
            .as_ref()
            .map(|transition| format!(" transition: {transition},"))
            .unwrap_or_default();
        out.push_str(&format!(
            "    {pattern} => generatedPage(location: route.location, name: '{}',{transition_arg} fullscreenDialog: {}, maintainState: {}, child: {child}),\n",
            route.name, route.annotation.fullscreen_dialog, route.annotation.maintain_state,
        ));
    }
    out.push_str("  };\n}\n");
}

fn render_constructor_param(param: &RouteParamSpec) -> String {
    if param.is_path || (!param.ty.is_nullable() && !param.has_default) {
        format!("required this.{}", param.name)
    } else if let Some(default_value) = &param.default_value_source {
        format!("this.{} = {default_value}", param.name)
    } else {
        format!("this.{}", param.name)
    }
}

fn is_not_found_route(route: &RouteSpec) -> bool {
    route.route_class == "NotFoundRoute" && route.params.iter().any(|param| param.name == "path")
}

pub(super) fn effective_shell<'a>(
    route: &'a RouteSpec,
    routes: &'a [RouteSpec],
) -> Option<&'a str> {
    route
        .annotation
        .shell
        .as_deref()
        .or_else(|| inherited_shell(route, routes))
}

fn inherited_shell<'a>(route: &RouteSpec, routes: &'a [RouteSpec]) -> Option<&'a str> {
    let current_segments = route_segments(&route.path);
    routes
        .iter()
        .filter(|candidate| candidate.path != route.path)
        .filter_map(|candidate| {
            let shell = candidate.annotation.shell.as_deref()?;
            let candidate_segments = route_segments(&candidate.path);
            (candidate_segments.len() < current_segments.len()
                && current_segments.starts_with(&candidate_segments))
            .then_some((candidate_segments.len(), shell))
        })
        .max_by_key(|(length, _)| *length)
        .map(|(_, shell)| shell)
}

fn route_segments(path: &str) -> Vec<&str> {
    path.split('/')
        .filter(|segment| !segment.is_empty())
        .collect()
}

fn render_location_body(out: &mut String, route: &RouteSpec) {
    if is_not_found_route(route) {
        out.push_str("    return '/404?path=${Uri.encodeComponent(path)}';\n");
        return;
    }

    let query_params = route
        .params
        .iter()
        .filter(|param| !param.is_path)
        .collect::<Vec<_>>();
    if !query_params.is_empty() {
        out.push_str("    final query = <String, String>{};\n");
        for param in query_params {
            let encode = encode_param_expr(&param.ty, &param.name);
            if param.ty.is_nullable() {
                out.push_str(&format!(
                    "    if ({0} != null) {{\n      query['{0}'] = {1};\n    }}\n",
                    param.name, encode
                ));
            } else if let Some(default_value) = &param.default_value_source {
                out.push_str(&format!(
                    "    if ({0} != {default_value}) {{\n      query['{0}'] = {1};\n    }}\n",
                    param.name, encode
                ));
            } else {
                out.push_str(&format!("    query['{}'] = {};\n", param.name, encode));
            }
        }
    }
    let segments = route
        .path
        .split('/')
        .filter(|segment| !segment.is_empty())
        .map(|segment| {
            if let Some(name) = segment.strip_prefix(':') {
                encode_param_expr(
                    route
                        .params
                        .iter()
                        .find(|param| param.name == name)
                        .map(|param| &param.ty)
                        .unwrap_or(&TypeIr::string()),
                    name,
                )
            } else {
                format!("'{segment}'")
            }
        })
        .collect::<Vec<_>>()
        .join(",\n      ");
    if route.params.iter().any(|param| !param.is_path) {
        out.push_str(&format!(
            "    return _routePath([\n      {segments},\n    ], queryParameters: query.isEmpty ? null : query);\n"
        ));
    } else if segments.is_empty() {
        out.push_str("    return '/';\n");
    } else {
        out.push_str(&format!(
            "    return _routePath([\n      {segments},\n    ]);\n"
        ));
    }
}

fn render_factory_params(route: &RouteSpec) -> String {
    let params = route
        .params
        .iter()
        .map(render_factory_param)
        .collect::<Vec<_>>()
        .join(", ");
    if route.params.iter().any(|param| param.is_named) {
        format!("{{{params}}}")
    } else {
        params
    }
}

fn render_factory_param(param: &RouteParamSpec) -> String {
    let ty = dart_type(&param.ty);
    if param.is_path || (!param.ty.is_nullable() && !param.has_default) {
        format!("required {ty} {}", param.name)
    } else if let Some(default_value) = &param.default_value_source {
        format!("{ty} {} = {default_value}", param.name)
    } else {
        format!("{ty} {}", param.name)
    }
}

fn render_route_args(route: &RouteSpec) -> String {
    route
        .params
        .iter()
        .map(|param| format!("{}: {}", param.name, param.name))
        .collect::<Vec<_>>()
        .join(", ")
}

fn route_constructor_with_fallback(route: &RouteSpec, fallback_expr: &str) -> String {
    if route.params.is_empty() {
        return format!("const {}()", route.route_class);
    }
    let args = route
        .params
        .iter()
        .map(|param| {
            let value = if param.is_path || (!param.ty.is_nullable() && !param.has_default) {
                fallback_value_for_param(param, fallback_expr)
            } else if let Some(default_value) = &param.default_value_source {
                default_value.clone()
            } else if param.ty.is_nullable() {
                "null".to_owned()
            } else {
                fallback_value_for_param(param, fallback_expr)
            };
            format!("{}: {value}", param.name)
        })
        .collect::<Vec<_>>()
        .join(", ");
    format!("{}({args})", route.route_class)
}

fn fallback_value_for_param(param: &RouteParamSpec, fallback_expr: &str) -> String {
    match &param.ty {
        TypeIr::Builtin {
            kind: BuiltinType::String,
            ..
        } => fallback_expr.to_owned(),
        TypeIr::Builtin {
            kind: BuiltinType::Int,
            ..
        } => "0".to_owned(),
        TypeIr::Builtin {
            kind: BuiltinType::Double,
            ..
        } => "0".to_owned(),
        TypeIr::Builtin {
            kind: BuiltinType::Bool,
            ..
        } => "false".to_owned(),
        _ => "null".to_owned(),
    }
}

fn render_parse_case(out: &mut String, route: &RouteSpec) {
    if is_not_found_route(route) {
        out.push_str("  if (segments.length == 1 && segments[0] == '404') {\n");
        out.push_str("    return NotFoundRoute(path: uri.queryParameters['path'] ?? '');\n");
        out.push_str("  }\n\n");
        return;
    }

    let path_segments = route
        .path
        .split('/')
        .filter(|segment| !segment.is_empty())
        .collect::<Vec<_>>();
    let mut conditions = vec![if path_segments.is_empty() {
        "segments.isEmpty".to_owned()
    } else {
        format!("segments.length == {}", path_segments.len())
    }];
    for (index, segment) in path_segments.iter().enumerate() {
        if !segment.starts_with(':') {
            conditions.push(format!("segments[{index}] == '{segment}'"));
        }
    }
    out.push_str(&format!("  if ({}) {{\n", conditions.join(" && ")));
    for (index, segment) in path_segments.iter().enumerate() {
        if let Some(name) = segment.strip_prefix(':') {
            let Some(param) = route.params.iter().find(|param| param.name == name) else {
                continue;
            };
            out.push_str(&format!(
                "    final {name} = {};\n",
                decode_path_expr(&param.ty, index)
            ));
            if !matches!(
                param.ty,
                TypeIr::Builtin {
                    kind: BuiltinType::String,
                    ..
                }
            ) {
                out.push_str(&format!("    if ({name} == null) {{\n"));
                out.push_str("      return _notFoundRoute(uri);\n");
                out.push_str("    }\n");
            }
        }
    }
    let mut args = Vec::new();
    for param in &route.params {
        if param.is_path {
            args.push(format!("{}: {}", param.name, param.name));
        } else {
            args.push(format!("{}: {}", param.name, decode_query_expr(param)));
        }
    }
    out.push_str(&format!(
        "    return {}({});\n",
        route.route_class,
        args.join(", ")
    ));
    out.push_str("  }\n\n");
}

fn encode_param_expr(ty: &TypeIr, name: &str) -> String {
    let access = if ty.is_nullable() {
        format!("{name}!")
    } else {
        name.to_owned()
    };
    match ty {
        TypeIr::Builtin {
            kind: BuiltinType::String,
            ..
        } => access,
        _ => format!("{access}.toString()"),
    }
}

fn decode_path_expr(ty: &TypeIr, index: usize) -> String {
    match ty {
        TypeIr::Builtin {
            kind: BuiltinType::String,
            ..
        } => format!("segments[{index}]"),
        TypeIr::Builtin {
            kind: BuiltinType::Int,
            ..
        } => format!("int.tryParse(segments[{index}])"),
        TypeIr::Builtin {
            kind: BuiltinType::Double,
            ..
        } => format!("double.tryParse(segments[{index}])"),
        TypeIr::Builtin {
            kind: BuiltinType::Bool,
            ..
        } => format!("_parseBool(segments[{index}])"),
        _ => "null".to_owned(),
    }
}

fn decode_query_expr(param: &RouteParamSpec) -> String {
    let name = &param.name;
    match &param.ty {
        TypeIr::Builtin {
            kind: BuiltinType::String,
            nullable: true,
        } => format!("uri.queryParameters['{name}']"),
        TypeIr::Builtin {
            kind: BuiltinType::String,
            nullable: false,
        } => format!(
            "uri.queryParameters['{name}'] ?? {}",
            param.default_value_source.as_deref().unwrap_or("''")
        ),
        TypeIr::Builtin {
            kind: BuiltinType::Int,
            nullable: true,
        } => format!("int.tryParse(uri.queryParameters['{name}'] ?? '')"),
        TypeIr::Builtin {
            kind: BuiltinType::Int,
            nullable: false,
        } => format!(
            "int.tryParse(uri.queryParameters['{name}'] ?? '') ?? {}",
            param.default_value_source.as_deref().unwrap_or("0")
        ),
        TypeIr::Builtin {
            kind: BuiltinType::Double,
            nullable: true,
        } => format!("double.tryParse(uri.queryParameters['{name}'] ?? '')"),
        TypeIr::Builtin {
            kind: BuiltinType::Double,
            nullable: false,
        } => format!(
            "double.tryParse(uri.queryParameters['{name}'] ?? '') ?? {}",
            param.default_value_source.as_deref().unwrap_or("0")
        ),
        TypeIr::Builtin {
            kind: BuiltinType::Bool,
            nullable: true,
        } => format!("_parseBool(uri.queryParameters['{name}'])"),
        TypeIr::Builtin {
            kind: BuiltinType::Bool,
            nullable: false,
        } => format!(
            "_parseBool(uri.queryParameters['{name}']) ?? {}",
            param.default_value_source.as_deref().unwrap_or("false")
        ),
        _ => "null".to_owned(),
    }
}

fn package_import_uri(library: &LibraryIr) -> Option<String> {
    let source = Path::new(&library.source_path);
    let relative = source
        .strip_prefix(&library.package_root)
        .ok()
        .and_then(|path| path.strip_prefix("lib").ok())
        .or_else(|| source.strip_prefix("lib").ok())?;
    let path = relative
        .components()
        .filter_map(|component| match component {
            std::path::Component::Normal(value) => value.to_str(),
            _ => None,
        })
        .collect::<Vec<_>>()
        .join("/");
    Some(format!("package:{}/{}", library.package_name, path))
}

fn dart_type(ty: &TypeIr) -> String {
    match ty {
        TypeIr::Builtin { kind, nullable } => {
            format!("{}{}", kind.as_str(), if *nullable { "?" } else { "" })
        }
        TypeIr::Named { name, nullable, .. } => {
            format!("{name}{}", if *nullable { "?" } else { "" })
        }
        TypeIr::Dynamic => "dynamic".to_owned(),
        TypeIr::Function {
            signature,
            nullable,
        } => format!("{signature}{}", if *nullable { "?" } else { "" }),
        TypeIr::Record { shape, nullable } => {
            format!("{shape}{}", if *nullable { "?" } else { "" })
        }
        TypeIr::Unknown => "Object?".to_owned(),
    }
}

fn upper_camel_identifier(value: &str) -> String {
    value
        .rsplit('.')
        .next()
        .unwrap_or(value)
        .split(|ch: char| ch == '_' || ch == '-' || ch.is_whitespace())
        .filter(|part| !part.is_empty())
        .map(|part| {
            let mut chars = part.chars();
            match chars.next() {
                Some(first) => first.to_uppercase().chain(chars).collect::<String>(),
                None => String::new(),
            }
        })
        .collect()
}
